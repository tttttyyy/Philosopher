#include "philo.h"

int	ft_putnbr(int i)
{
	char	l;

	if (i < 0)
	{
		write(1, "-", 1);
		i *= -1;
	}
	if (i >= 10)
		ft_putnbr(i / 10);
	l = '0' + (i % 10);
	if (!write(1, &l, 1))
		return (0);
	else
		return (1);
}
