#include "philo.h"

int	ft_atoi(const char *str)
{
	long	res;
	size_t	i;

	i = 0;
	res = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '+')
		i++;
	while (str[i])
	{
		if (str[i] >= '0' && str[i] <= '9')
		{
			if (res > 2147483647)
				return (0);
			res = (res * 10) + str[i] - 48;
		}
		else
			return (0);
		i++;
	}
	return (res);
}
